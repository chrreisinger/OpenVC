        ALOAD(0)
        INVOKESPECIAL("java/lang/Object", "<init>", "()V")
        for ((symbol, i) <- combinedList zipWithIndex) {
          ALOAD(0)
          loadInstruction(symbol.dataType, i + 1)
          PUTFIELD(cw.className, symbol.name, getJVMDataType(symbol))
